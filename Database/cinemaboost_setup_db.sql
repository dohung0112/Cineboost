CREATE FUNCTION [dbo].[fn_PickRandomChar]
(
  @chars VARCHAR(MAX)
)
RETURNS CHAR(1)
AS
BEGIN
  DECLARE @result CHAR(1) = NULL;
  DECLARE @resultIndex INT = NULL;
    SET @resultIndex = [dbo].[fn_RandIntBetween](1, LEN(@chars));
    SET @result = SUBSTRING(@chars, @resultIndex, 1);
  RETURN @result;
END

